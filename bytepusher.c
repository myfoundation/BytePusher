uint8_t mem[MEM_SIZE];
uint32_t screen[W * H];

#define GET3(mem, i) (mem[i] << 16 | mem[i + 1] << 8 | mem[i + 2])

void sim(uint8_t *mem, int cycles) {
    int pc = GET3(mem, 2);
    for (int i = 0; i < cycles; i++) {
        mem[GET3(mem, pc + 3)] = mem[GET3(mem, pc)];
        pc = GET3(mem, pc + 6);
    }
}

int main(int argc, char **argv) {
    assert(argc == 2);
    FILE *fp = fopen(argv[1], "rb");
    assert(fread(mem, 1, MEM_SIZE, fp) <= MEM_SIZE);
    fclose(fp);
    InitWindow(W * SCALE, H * SCALE, "");
    Image image = {
        .data = screen,
        .width = W,
        .height = H,
        .format = PIXELFORMAT_UNCOMPRESSED_R8G8B8A8,
        .mipmaps = 1
    };
    Texture2D texture = LoadTextureFromImage(image);
    SetTargetFPS(60);
    while(!WindowShouldClose()) {
        sim(mem, 65536);
        int start = mem[5] << 16;
        for (int i = 0; i < W * H; i++) {
            screen[i] = 0xff000000 | colors[mem[start + i]];
        }
        UpdateTexture(texture, screen);
        BeginDrawing();
        DrawTextureEx(texture, (Vector2) {0, 0}, 0, SCALE, WHITE);
        EndDrawing();
    }
    UnloadTexture(texture);
    CloseWindow();
    return 0;
}
